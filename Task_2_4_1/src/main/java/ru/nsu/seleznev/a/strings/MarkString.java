package ru.nsu.seleznev.a.strings;

/**
 * MarkString class for reading dsl config.
 */
public class MarkString {
  private String mark;
  private String date;

  /**
   * Empty constructor.
   */
  public MarkString() {
  }

  /**
   * Function that returns mark.
   *
   * @return String mark
   */
  public String getMark() {
    return mark;
  }

  /**
   * Function that returns date.
   *
   * @return String date
   */
  public String getDate() {
    return date;
  }
}
